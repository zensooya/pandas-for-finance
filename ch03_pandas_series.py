"""3.4 시리즈 인덱싱: 인덱싱은 하나의 값에 접근하는 것
*   리스트는 인덱스로만 인덱싱 했지만 시리즈는 행 번호와 인덱스 사용 가능 (시리즈는 행 번호 자동 부여)
*  행 번호 인덱싱 : iloc 연산 = 리스트 인덱싱과 동일
*  인덱스 인덱싱: loc 연산

"""

# 행 번호 인덱싱
data = [1000, 2000, 3000]
s = Series(data=data)

print(s.iloc[0])
print(s.iloc[1])
print(s.iloc[2])
print(s.iloc[-1])

# 인덱스 인덱싱
print(s.loc[0])
print(s.loc[1])
print(s.loc[2])
#print(s.loc[-1]) -> 에러. -1이라는 인덱스가 시리즈에 없기 때문.

# 문자열 인덱스 인덱싱
index = ["메로나", "구구콘", "하겐다즈"]
s = Series(data, index)

print(s.iloc[0])
print(s.loc["메로나"])

#[]만 이용할 수도. 알맞는 데이터를 찾아서 반환함.
print(s['메로나'])
print(s[0])

#[]는 기본적으로 loc 호출. 그치만 문자열 -> iloc
s1 = Series([10, 20, 30])
s2 = Series([10, 20, 30], index=[1,2,3])
s3 = Series([10,20,30], index=['a','b','c'])
print(s1[0])
#print(s2[0])
print(s3[0])
# =>  헷갈리니까 이렇게 하지 말자.

#시리즈 인덱스도 []인덱싱 쓸 수도 있다. 숫자일 때만.
print(s3.index[0])
print(s3.index[-1])

