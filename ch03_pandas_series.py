"""3.10 정렬 및 순위"""

data = [3.1, 2.0, 10.1, 5.1]
index = ['000010', '000020', '000030', '000040']
s = Series(data, index=index)
print(s)

# 정렬 (오름차순)
s1 = s.sort_values()
