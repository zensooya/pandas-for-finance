"""6.5 데이터 샘플링"""

import pandas as pd

df=pd.read_csv("data_1724_20260718.csv", encoding="euc-kr", index_col=0)
df.index=pd.to_datetime(df.index)
df=df.sort_index()[['시가','저가','고가','종가','거래량']]
df.head()

df.resample('M').first() # resample 메서드: 입력된 파라미터에 따라 데이터를 샘플링, 인덱스가 타임스탬프여야함.
# resample('M'): 월 단위로 데이터를 그룹핑
# first 메서드: 그룹핑한 데이터에서 첫번째를 대푯값으로 쓰겠다.

df.resample('MS').first() # S 옵션 추가: 그룹 중 가장 빠른 일자로 인덱스를 설정

how = {
    '시가':'first',
    '종가':'last',
    '고가':'max',
    '저가':'min',
    '거래량':'sum',
}
df.resample('MS').agg(how)

df.resample('3D').agg(how) # 3일단위로 그룹화. 시(H)/분(T)/초(S)/월(M)/일(D)

# 일/시/분/초에서는 S 옵션 안됨.
temp = df.resample('3D').agg(how)
temp.index=temp.index + pd.to_timedelta('2D') # Timedelta 객체로 시간 차이 보정하기. 모든 날짜에 2일차 시간 보정
temp.dropna() # dropna 메서드: 결측값 제거

df.resample('3D',offset='1D').agg(how) # resample 메서드의 offset 파라미터. 기준 일자를 조정. 1일 미루기.

df.resample('12H').agg(how) # 일봉 데이터를 12시간 단위의 데이터로 업샘플링

# 다운샘플링(Downsampling): 데이터 개수를 줄이는 것 (짧은 주기 → 긴 주기)
# 업샘플링(Upsampling): 데이터 개수를 늘리는 것 (긴 주기 → 짧은 주기)

df.resample('12H').agg(how).ffill() # ffill 메서드: 결측값을 이전 로우의 값으로 대체 (forward fill)
# bfill 메서드: 아래 행의 값으로 결측값 채워 넣음. backward fill

# NaN이 아니라 0으로 표시된다. sum 함수가 결측값을 0으로 합사하기 때문이다.

import numpy as np

temp = df.resample('12H').agg(how)
cond = temp.isna().any(axis=1)
# isna 메서드: NaN인 값은 True, 아닌 경우는 False 표기. 데이터프레임 반환
# ant 메서드: 하나라도 True면 True 반환. axis=1 옵션: 로우 단위로 비교
temp.loc[cond,'거래량']=np.nan # 하나라도 nan이 있는 행의 거래량을 nan으로 업데이트

temp.ffill()

"""6.6 수익률 계산하기"""

import pandas as pd

data = {'삼성전가':[52200, 52300, 52900, 52000, 51700],
        'LG전자':[68200,67800,68800,67500,66300]}
df=pd.DataFrame(data=data)
