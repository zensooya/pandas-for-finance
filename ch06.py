#  보유 후 2일 뒤 매도하는 전략의 누적 수익률 계산하기
yeild = df.pct_change(periods=2)+1 # 인접한 두 값의 수익률을 얻어와서 원금 1을 더한 시리즈
yeild.cumprod() # cumprod 메서드: 시리즈에 대해서 누적곱 리턴

