"""4.7 데이터프레임 필터링"""

data = [
    ['3R', 1510, 7.36],
    ['3SOFT', 1790, 1.65],
    ['ACTS', 1185, 1.28]
]
columns = ["종목명", "현재가", "등락률"]
index = ["037730", "036360", "005760"]

df = DataFrame(data=data, index=index, columns=columns)
cond = df['현재가'] >= 1400
print(df.loc[cond]) # 데이터 프레임 슬라이싱의 결과 = 데이터프레임

#칼럼의 이름으로 인덱실
print(df.loc[cond]['현재가'])
print(df.loc[cond,'현재가']) # 위 라인보다 이 방법이 더 빠르다.

# 복잡한 조건 -> 논리연산자
cond = (df['현재가'] >= 1400) & (df['현재가'] < 1700) # & 쓸 때는 무조건 괄호 구분
print(df.loc[cond])
print(df.loc[~cond]) # not 연산자

