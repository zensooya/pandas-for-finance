# 정수로 변경하기
def remove_comma(x):
  return int(x.replace(',',''))

df['03/02']=df['03/02'].map(remove_comma)
df['03/03']=df['03/03'].map(remove_comma)
df['03/04']=df['03/04'].map(remove_comma)
