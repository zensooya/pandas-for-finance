"""5.5 GroupBy

① 무엇으로 묶을 것인가?

groupby("테마")

↓

② 어떤 컬럼을 계산할 것인가?

["PER"]

↓

③ 어떤 계산을 할 것인가?

.mean()
.max()
.min()
.sum()
.count()
.agg(...)
"""

from pandas import DataFrame

data = [
    ["2차전지(생산)","SK이노베이션", 10.19,1.29],
    ["해운","팬오션",21.23,0.95],
    ["시스템반도체","티엘아이",35.97,1.12],
    ["해운","HMM",21.52,3.20],
    ["시스템반도체","아이에이",37.32,3.55],
    ["2차전지(생산)","LG화학",83.06,3.75]
]

columns=["테마","종목명","PER","PBR"]
df=DataFrame(data=data,columns=columns)

# 테마별로 종목을 분류하기
df1=df[df['테마']=="2차전지(생산)"]
print(df1)

df2=df[df['테마']=="해운"]
print(df2)

df3=df[df['테마']=="시스템반도체"]
print(df3)

# 테마별로 그룹화한 데이터프레임에서 PER 컬럼의 평균값 계산하기
# 데이터 프레임의 컬럼은 시리즈 객체.
# 시리즈 객체의 mean 매서드 이용
mean1 = df1['PER'].mean()
mean2 = df2['PER'].mean()
mean3 = df3['PER'].mean()

# PER 평균을 시리즈로 저장하기
data = [mean1,mean2,mean3]
index = ["2차전지(생산)","해운","시스템반도체"]
s = pd.Series(data=data,index=index)
print(s)

# 위의 과정: 테마별로 split -> 계산 apply -> 결과를 combine
# groupby 메서드로 간소화할 수 있다.
df.groupby("테마")["PER"].mean()

# 2차 전지 테마의 데이터만 선택하기
gb = df.groupby("테마")
temp = gb.get_group("2차전지(생산)")
print(temp)

# 종목명을 제외하고 PER과 PBR만 선택하기
temp = df[["테마","PER","PBR"]].groupby("테마").get_group("2차전지(생산)")
print(temp)

# DataFrameGoupBy 객체를 슬라이싱하기
temp = df.groupby("테마")[["PER","PBR"]].get_group("2차전지(생산)")
print(temp)

# 여러 개의 컬럼 + 집계 기능
# groupby(그룹화)-> PER, PBR에 mean 메서드 -> 데이터프레임 반환
df.groupby("테마")[["PER","PBR"]].mean()

# 데이터 프레임을 그룹화한 후 특정 칼럼마다 다른 함수를 적용하기
df.groupby("테마").agg({"PER":max,"PBR":min}) # aggregate = 모으다, 집계하다, 종합하다

# 하나의 컬럼에 여러 연산을 지정하기
import numpy as np
df.groupby("테마").agg({"PER":[min,max],"PBR":[np.std,np.var]})

