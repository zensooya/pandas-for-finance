# MultiIndex의 from_product 함수-> 입력된 리스트로 데이터의 조합을 만든다
import pandas as pd

level_0 = ['영업이익', '당기순이익']
level_1 = ['컨센서스', '잠정치']

idx = pd.MultiIndex.from_product([level_0,level_1])

print(idx)
print(idx.get_level_values(0))

